export function createKButton(keyboard, capsLock) {
  let keyButton = '';
  KeyboardButtonsArr.forEach((button) => {
    keyButton = document.createElement('div');
    keyButton.className = 'keyboard-button';
    if (button.code === 'Space') {
      keyButton.classList.add(button.code);
      keyButton.id = button.code;
    }
    if (button.code.includes('Key')) {
      keyButton.classList.add('keyboard-letter');
      keyButton.id = button.code;
    } else if (button.key.length > 1) {
      keyButton.classList.add('special-btn');
      keyButton.classList.add(button.key);
      keyButton.id = button.code;
    }
    if (button.code.includes('ArrowLeft')) {
      keyButton.innerHTML = '&#8592';
    } else if (button.code.includes('ArrowRight')) {
      keyButton.innerHTML = '&#8594';
    } else if (button.code.includes('ArrowUp')) {
      keyButton.innerHTML = '&#8593';
    } else if (button.code.includes('ArrowDown')) {
      keyButton.innerHTML = '&#8595';
    } else if (button.code.includes('Meta')) {
      keyButton.innerHTML = 'Win';
    } else if (button.code.includes('Control')) {
      keyButton.innerHTML = 'Ctrl';
    } else {
      keyButton.textContent = capsLock ? button.key.toUpperCase() : button.key;
      keyButton.id = button.code;
    }
    keyboard.appendChild(keyButton);
  });
}
